class User {
  final String id;
  final String fname;
  final String lname;
  final String empId;
  final String email;
  final String mobile;
  final String departmentName;

  User({
    required this.id,
    required this.fname,
    required this.lname,
    required this.empId,
    required this.email,
    required this.mobile,
    required this.departmentName,
  });
}
